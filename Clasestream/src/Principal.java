    private void fSButtonMD1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fSButtonMD1ActionPerformed
        // TODO add your handling code here:
        long startTime = System.currentTimeMillis();
        modelo.setRowCount(0);
        lista.stream().filter(persona -> persona.getNombre().equalsIgnoreCase(dd.getText())).sorted(Comparator.comparing(Persona::getNombre)).forEach(persona -> {
            Object[] fila = {persona.getNombre(), persona.getPriape(), persona.getSeguape()};
            modelo.addRow(fila);
            encontrado = true;
        });
        tabla1.setModel(modelo);
        dd.setText("");
        if (!encontrado) {
            JOptionPane.showMessageDialog(null, "No se encontró ninguna persona con ese nombre");
        }
        long duracion = System.currentTimeMillis() - startTime;
        tablaa.setText("La duración del proceso de buscar por nombre es de " + duracion + " milisegundos");
    }//GEN-LAST:event_fSButtonMD1ActionPerformed

