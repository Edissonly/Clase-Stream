    public void leer() throws FileNotFoundException {
        long startTime = System.currentTimeMillis();
        File archivo = new File("src/archivo.txt");
        Scanner scanner = new Scanner(archivo);
        while (scanner.hasNextLine()) {
            String linea = scanner.nextLine();
            String[] partes = linea.split(" ");
            Persona persona = new Persona();
            persona.setNombre(partes[0]);
            persona.setPriape(partes[1]);
            persona.setSeguape(partes[2]);
            lista.add(persona);
        }
        scanner.close();
        long duration = System.currentTimeMillis() - startTime;
        System.out.println("La duración del proceso de leer es de " + duration + " milisegundos");
    }

    public void mostrar() {
        long startTime = System.currentTimeMillis();
        modelo.setRowCount(0);
        lista.stream().sorted(Comparator.comparing(Persona::getNombre)).forEach(persona -> {
            Object[] fila = {persona.getNombre(), persona.getPriape(), persona.getSeguape()};
            modelo.addRow(fila);
        });
        tabla1.setModel(modelo);
        long duration = System.currentTimeMillis() - startTime;
        tablaa.setText("La duración del proceso de mostrar en la tabla es de " + duration + " milisegundos");
    }

